document.addEventListener('DOMContentLoaded', function() {
    // Translate the title first
    const titleElement = document.querySelector('title[data-translate-key]');
    if (titleElement) {
        const key = titleElement.getAttribute('data-translate-key');
        if (translations[key]) {
            document.title = translations[key];
        }
    }

    // Translate all other elements
    const elements = document.querySelectorAll('[data-translate-key]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate-key');
        if (translations[key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[key];
            } else if (element.tagName !== 'TITLE') { // Don't re-translate title
                element.innerHTML = translations[key];
            }
        }
    });
});